def get_results_i18n(language_code: Optional[str]) -> dict[str, str]:
    """Return merged result labels with English fallback."""
    canonical = normalize_language_code(language_code, default="en")
    if canonical == "fr-CA":
        merged = {**_EN, **_FR, **_FR_CA}
        return merged
    return {**_EN, **_TRANSLATIONS.get(canonical, {})}
